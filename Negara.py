class Negara:

  def __init__(self, Nama_Negara, Benua, Jenis_Pemerintahan, Luas_Negara, Jumlah_Penduduk):
    self.Nama_Negara = Nama_Negara
    self.Benua = Benua
    self.Jenis_Pemerintahan = Jenis_Pemerintahan
    self.Luas_Negara = Luas_Negara
    self.Jumlah_Penduduk = Jumlah_Penduduk

class Pemimpin:
  def __init__(self, Jenis_pemimpin, Nama_pemimpin, Tanggal_Lahir_Pemimpin, Ciri_Khas_Pemimpin):
    self.Jenis_pemimpin = Jenis_pemimpin
    self.Nama_pemimpin = Nama_pemimpin
    self.Tanggal_Lahir_Pemimpin = Tanggal_Lahir_Pemimpin
    self.Ciri_Khas_Pemimpin = Ciri_Khas_Pemimpin